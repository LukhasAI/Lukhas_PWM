def bootstrap_seed_chain(seed: str, depth: int = 3) -> list[str]:
    """Generate a deterministic chain of seeds."""
    chain = [seed]
    for _ in range(depth - 1):
        seed = str(hash(seed))
        chain.append(seed)
    return chain

